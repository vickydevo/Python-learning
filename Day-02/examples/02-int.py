# Integer variables
num1 = 33
num2 = 6

# Integer Division
result1 = num1 / num2
print("Integer Division:", result1)

# Modulus (Remainder)
result2 = num1 % num2
print("Modulus (Remainder):", result2)

# Absolute Value
result3 = abs(-11)
print("Absolute Value:", result3)

# addition
result4 = num1 + num2
print("Additon of 2 numbers:", result4 )
